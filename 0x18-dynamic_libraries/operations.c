#include <stdio.h>

/**
 * add - add func
 *
 * @a: input
 * @b: input
 *
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub func
 *
 * @a: input
 * @b: input
 *
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply func
 *
 * @a: input
 * @b: input
 *
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div func
 *
 * @a: input
 * @b: input
 *
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod func
 *
 * @a: input
 * @b: input
 *
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
